/**
 * Generate daily report content
 */
const generateDailyReport = (data) => {
  const { stats, records } = data;

  return `
    <div class="stats-grid">
      <div class="stat-card">
        <div class="value">${stats.totalRecords || 0}</div>
        <div class="label">Total Records</div>
      </div>
      <div class="stat-card">
        <div class="value">${(stats.totalReceived || 0).toLocaleString()}</div>
        <div class="label">Records Received</div>
      </div>
      <div class="stat-card">
        <div class="value">${(stats.totalProcessed || 0).toLocaleString()}</div>
        <div class="label">Records Processed</div>
      </div>
      <div class="stat-card">
        <div class="value">${(stats.totalPending || 0).toLocaleString()}</div>
        <div class="label">Pending Records</div>
      </div>
      <div class="stat-card">
        <div class="value">${stats.pendingCount || 0}</div>
        <div class="label">Pending Items</div>
      </div>
      <div class="stat-card">
        <div class="value">${stats.discrepancyCount || 0}</div>
        <div class="label">Discrepancies</div>
      </div>
    </div>
    <h2>Daily Records</h2>
    ${records && records.length > 0 ? `
      <table>
        <thead>
          <tr>
            <th>ID</th>
            <th>Unit</th>
            <th>Department</th>
            <th>Category</th>
            <th>Received</th>
            <th>Processed</th>
            <th>Status</th>
          </tr>
        </thead>
        <tbody>
          ${records.map(r => `
            <tr>
              <td>${r.recordId || '-'}</td>
              <td>${r.unit || '-'}</td>
              <td>${r.department?.name || '-'}</td>
              <td>${r.category?.name || '-'}</td>
              <td>${(r.recordsReceived || 0).toLocaleString()}</td>
              <td>${(r.recordsProcessed || 0).toLocaleString()}</td>
              <td class="status-${r.status}">${r.status}</td>
            </tr>
          `).join('')}
        </tbody>
      </table>
    ` : '<p>No records found for this date.</p>'}
  `;
};

/**
 * Generate weekly report content
 */
const generateWeeklyReport = (data) => {
  const { stats, records } = data;

  return `
    <div class="stats-grid">
      <div class="stat-card">
        <div class="value">${stats.totalRecords || 0}</div>
        <div class="label">Total Records</div>
      </div>
      <div class="stat-card">
        <div class="value">${(stats.totalReceived || 0).toLocaleString()}</div>
        <div class="label">Records Received</div>
      </div>
      <div class="stat-card">
        <div class="value">${(stats.totalProcessed || 0).toLocaleString()}</div>
        <div class="label">Records Processed</div>
      </div>
    </div>
    <h2>Weekly Summary (${stats.weekStart} - ${stats.weekEnd})</h2>
    ${stats.dailyData && stats.dailyData.length > 0 ? `
      <table>
        <thead>
          <tr>
            <th>Date</th>
            <th>Count</th>
            <th>Received</th>
            <th>Processed</th>
          </tr>
        </thead>
        <tbody>
          ${stats.dailyData.map(d => `
            <tr>
              <td>${d._id}</td>
              <td>${d.count}</td>
              <td>${(d.totalReceived || 0).toLocaleString()}</td>
              <td>${(d.totalProcessed || 0).toLocaleString()}</td>
            </tr>
          `).join('')}
        </tbody>
      </table>
    ` : '<p>No data available for this week.</p>'}
  `;
};

