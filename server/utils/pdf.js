/**
 * Generate records report content
 */
const generateRecordsReport = (data) => {
  const records = Array.isArray(data) ? data : [];

  return `
    <h2>Records Report</h2>
    <p>Total Records: ${records.length}</p>
    ${records.length > 0 ? `
      <table>
        <thead>
          <tr>
            <th>ID</th>
            <th>Unit</th>
            <th>Department</th>
            <th>Category</th>
            <th>Date</th>
            <th>Received</th>
            <th>Processed</th>
            <th>Pending</th>
            <th>Status</th>
          </tr>
        </thead>
        <tbody>
          ${records.map(r => `
            <tr>
              <td>${r.recordId || '-'}</td>
              <td>${r.unit || '-'}</td>
              <td>${r.department?.name || r.department || '-'}</td>
              <td>${r.category?.name || r.category || '-'}</td>
              <td>${r.date ? new Date(r.date).toLocaleDateString() : '-'}</td>
              <td>${(r.recordsReceived || 0).toLocaleString()}</td>
              <td>${(r.recordsProcessed || 0).toLocaleString()}</td>
              <td>${(r.pendingRecords || 0).toLocaleString()}</td>
              <td class="status-${r.status}">${r.status}</td>
            </tr>
          `).join('')}
        </tbody>
      </table>
    ` : '<p>No records to display.</p>'}
  `;
};

/**
 * Generate general report content
 */
const generateGeneralReport = (data) => {
  return `
    <h2>Report Data</h2>
    <pre>${JSON.stringify(data, null, 2)}</pre>
  `;
};

